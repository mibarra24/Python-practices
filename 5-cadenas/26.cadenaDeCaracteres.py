# Cadena de caracteres

#Formas de poner la cadena:

#Con comillas:

cadena = "Estoy \"estudiando\""

#Con 4 espacios:

cadena1 = "Estoy \testudiando"

#Salto de linea:

cadena2 = "Estoy \nestudiando"

#Mensaje grabado en crudo(no procesar):

cadena3 = r"D:\nombre\trabajos"

#Cadenas en muchas lineas:

cadena4 = """Hola
que tal?
Mi nombre es Marco"""

#Combinar cadenas:

cadena5 = "Arriba las"
cadena6 = " Chivas"

print(cadena)
print(cadena1)
print(cadena2)
print(cadena3)
print(cadena4)
print(cadena5 + cadena6)