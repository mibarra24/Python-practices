# Bucle tipo Range: se repite las veces que les indiquemos

for i in range(50):
    print(i)

#Mostrar numeros del 5 al 10
for i in range(5,11):
    print(i)

#Mostrar numeros pares del 2 al 20
for i in range(2,21,2):
    print(i)

#Asi se indica cuantas veces se repite el bucle y de donde a donde
for i in range(20,1,-2):
    print(i)

